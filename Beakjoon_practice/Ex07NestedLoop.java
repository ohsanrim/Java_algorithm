package Day03;
//NestedLoop
//다중 forloop





public class Ex07NestedLoop {

    public static void main(String[] args) {
        //다중 for loop을 만들어 봅시다
       for(int i=1;i<=5;i++) {
           for(int j=101;j<=105;j++) {
               System.out.print("i의 값: "+i);
               System.out.println (", j의 값: "+ j);
           }
       }

    }

}
